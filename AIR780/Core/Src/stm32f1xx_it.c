/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE)!=RESET)
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		HAL_UART_DMAStop(&huart1);
		rxindex=sizeof(rx1_buf);
		if(rxindex==0) memset(&rx1_buf,0,sizeof(rx1_buf));
		else {
			if(rx1_buf[0]=='S'&&datasastus==0) {
				datasastus=1;
				dataindex++;
			}
			if(datasastus==1){
				while(dataindex<20&&datasastus==1)
				{
					if(rx1_buf[dataindex]!='E'){
						buff[dataindex]=rx1_buf[dataindex];
						
					}
					dataindex++;
				}
				
				restore_float_from_parts(buff,RestoreData,5);
				memset(&buff,0,sizeof(buff));				
			}
		}
		datasastus=0;
		dataindex=0;
		rxindex=0;
		memset(&rx1_buf,0,sizeof(rx1_buf));
		HAL_UART_Receive_DMA(&huart1,(uint8_t*)rx1_buf,100);
	}
	
	
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

